                        In software engineering, software configuration management (SCM or S/W CM) is the task of
                        tracking and controlling changes in the software, part of the larger cross-disciplinary
                        field of
                        configuration management.[1] SCM practices include revision control and the establishment of
                        baselines. If something goes wrong, SCM can determine the "what, when, why and who" of the
                        change. If a configuration is working well, SCM can determine how to replicate it across
                        many
                        hosts.

                        The acronym "SCM" is also expanded as source configuration management process and software
                        change and configuration management.[2] However, "configuration" is generally understood to
                        cover changes typically made by a system administrator.
